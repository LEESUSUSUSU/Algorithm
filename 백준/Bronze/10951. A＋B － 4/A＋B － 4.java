


import java.util.Scanner;

public class Main {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);

		while (sc.hasNext()) {  // 나가는 방법을 몰라서 알아봄 (정수 외 값이 나오면 false 나온다.)
			int a = sc.nextInt();
			int b = sc.nextInt();

			System.out.println(a + b);

		}

	}
}
